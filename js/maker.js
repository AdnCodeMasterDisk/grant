
const conteditable = document.querySelector(".editable");

function openblokscontainer(){
    document.querySelector('.contbloues').classList.remove('hide');
    document.querySelector('.constructor').classList.add('hide');
    document.querySelector('.btnexit').classList.remove('hide');
}

function exitbloques(){
    document.querySelector('.contbloues').classList.add('hide');
    document.querySelector('.constructor').classList.remove('hide');
    document.querySelector('.btnexit').classList.add('hide');
}

